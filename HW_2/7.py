x = int(input())

# начинаем с i = 2
# и count = 2 (делители 1 и само число)
i, count = 2, 2

# если число = 1, то кол-во делителей,
# которое нам известно, = 1
if x == 1:
    count = 1

# Пример: если число = 32, то делится на 4(i), и на 16(i*i)
while i * i <= x:
    if x % i == 0:
        count += 1
        # если квадрат числа не равен самому числу, то count++
        if i * i != x:
            count += 1
    i += 1

print(count)
